f = @(x) sin(x+pi/4).^2-x.^3+pi/4*x.^2+5*pi^2/16*x+3*pi^3/64;
g1 = @(x) 16/(5*pi^2)*(-sin(x+pi/4).^2+x.^3-pi*x.^2/4-3*pi^3/64);
g2 = @(x) cbrt(sin(x+pi/4).^2+pi/4*x.^2+5*pi^2/16*x+3*pi^3/64);
xx=linspace(-2,3,25);
plot(xx,f(xx),'b-',...
     xx,g1(xx),'b.-',...
     xx,g2(xx),'b+-',...
     xx,xx,'r--',...
     [-pi/4,3*pi/4],[0,0],'ro',
     [-pi/4,3*pi/4],[-pi/4,3*pi/4],'r.');grid
legend("f","g1","g2","y=x","zeros","pt.fixos","location","southwest")
set(gca,"fontsize",12)
