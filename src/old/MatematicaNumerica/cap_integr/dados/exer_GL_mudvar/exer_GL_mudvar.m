f = @(x) (sin(x+2)-exp(-x.^2))./(x.^2+log(x+2));
a=0;
b=1;
F = @(u) (b-a)/2*f((b-a)/2*(u+1)+a);

#GL-1
x=0;
w=2;
s=w*F(x);
printf("%1.5E\n",s)

#GL-2
x=[sqrt(3)/3];
w=[1];
s=w(1)*F(x(1));
s+=w(1)*F(-x(1));
printf("%1.5E\n",s)

#GL-3
x=[0 sqrt(3/5)];
w=[8/9 5/9];
s=w(1)*F(x(1)) + w(2)*F(x(2));
s+=w(2)*F(-x(2));
printf("%1.5E\n",s)

#GL-4
x=[sqrt(3/7-2/7*sqrt(6/5)) sqrt(3/7+2/7*sqrt(6/5))];
w=[(18+sqrt(30))/36 (18-sqrt(30))/36];
s=w(1)*F(x(1)) + w(2)*F(x(2));
s+=w(1)*F(-x(1)) + w(2)*F(-x(2));
printf("%1.5E\n",s)

