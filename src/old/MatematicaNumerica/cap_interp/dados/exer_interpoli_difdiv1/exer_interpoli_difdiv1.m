x = [0 1 1.5 2]';
y = exp(x);

F = zeros(4,4);

F(:,1) = y;

F(2,2) = (F(2,1)-F(1,1))/(x(2)-x(1));
F(3,2) = (F(3,1)-F(2,1))/(x(3)-x(2));
F(4,2) = (F(4,1)-F(3,1))/(x(4)-x(3));

F(3,3) = (F(3,2)-F(2,2))/(x(3)-x(1));
F(4,3) = (F(4,2)-F(3,2))/(x(4)-x(2));

F(4,4) = (F(4,3)-F(3,3))/(x(4)-x(1));

p = F(1,1);
p = [0 p] + F(2,2)*poly(x(1));
p = [0, p] + F(3,3)*poly([x(1),x(2)])
p = [0, p] + F(4,4)*poly([x(1),x(2),x(3)])